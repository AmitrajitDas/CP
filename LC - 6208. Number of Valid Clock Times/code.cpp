#include<bits/stdc++.h>
using namespace std;


class Solution {
public:
	int countTime(string time) {
		int n = time.size();

		vector<string> hm;

		stringstream ss(time);
		string word;
		while (!ss.eof()) {
			getline(ss, word, ':');
			hm.push_back(word);
		}

		int res = 1;
		for (int i = 0; i < hm.size(); i++) {
			if (i == 0) {
				if (hm[i][0] == '?' && hm[i][1] == '?') res *= 24;
				else if (hm[i][0] == '?') {
					if (hm[i][1] > '3') res *= 2;
					else res *= 3;
				}
				else if (hm[i][1] == '?') {
					if (hm[i][0] < '2') res *= 10;
					else res *= 4;
				}
				else res *= 1;
			}
			else {
				if (hm[i][0] == '?' && hm[i][1] == '?') res *= 60;
				else if (hm[i][0] == '?') res *= 6;
				else if (hm[i][1] == '?') res *= 10;
				else res *= 1;
			}
		}

		return res;
	}
};

int main() {
#ifndef ONLINE_JUDGE
	freopen("input.txt", "r", stdin);
	freopen("output.txt", "w", stdout);
#endif

	int t;
	cin >> t;

	while (t--) {
		string str;
		cin >> str;

		Solution sol;
		int res = sol.countTime(str);
		cout << res << endl;

	}

	return 0;
}

