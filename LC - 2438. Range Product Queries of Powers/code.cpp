class Solution {
public:
	vector<int> productQueries(int n, vector<vector<int>>& queries) {
		int mod = 1e9 + 7;
		vector<int> temp;
		vector<int> powers;

		while (n > 0) {
			temp.push_back((int)n % 2);
			n /= 2;
		}

		for (int i = 0; i < temp.size(); i++) {
			if (temp[i] == 1) {
				int t = pow(2, i);
				powers.push_back(t);
			}
		}
		vector<int> ans(queries.size());
		int ind = 0;

		for (auto row : queries) {
			long mult = 1;
			for (int i = row[0]; i <= row[1]; i++) {
				mult = (long)(mult * powers[i] % mod);
			}
			ans[ind++] = ((int)mult % mod);
		}

		return ans;
	}
};

